//! Style Constants for Sage UI
//!
//! Common style patterns and layout constants.

/// Common style patterns
pub struct Styles;

impl Styles {
    /// Standard padding for content
    pub const CONTENT_PADDING: f32 = 1.0;

    /// Standard gap between elements
    pub const ELEMENT_GAP: f32 = 1.0;

    /// Indent for nested content (2 spaces to align with text after icon)
    pub const INDENT: f32 = 2.0;

    /// Small padding
    pub const PADDING_SMALL: f32 = 0.5;

    /// Large padding
    pub const PADDING_LARGE: f32 = 2.0;
}
